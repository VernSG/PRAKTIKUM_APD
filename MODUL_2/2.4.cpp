#include <iostream>
using namespace std;

int main() {
    // Operasi aritmatika dasar
    cout << "9 - 4 = " << 9 - 4 << endl;        /* mencetak hasil 9-4 */
    cout << "9 * 4 = " << 9 * 4 << endl;        /* mencetak hasil 9*4 */
    cout << "9 / 4 = " << 9 / 4 << endl;        /* mencetak hasil 9 div 4 */
    cout << "9.0 / 4 = " << 9.0 / 4 << endl;    /* mencetak hasil 9/4 */
    cout << "9 % 4 = " << 9 % 4 << endl;        /* mencetak hasil 9%4 */
    
    return 0;
}